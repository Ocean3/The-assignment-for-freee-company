    private void DeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteMouseClicked
        String option = JOptionPane.showInputDialog(null, "Which accout you want to remove?\nPlease type user name.");
        File file = new File("Textfiles/Customer.txt");
        ArrayList<String> str = take_out(file);
        for(int i = 0; i < str.size(); i++){
            if(str.get(i).equals(option)){
                str.remove(i - 5);
                str.remove(i - 5);
                str.remove(i - 5);
                str.remove(i - 5);
                str.remove(i - 5);
                str.remove(i - 5);
                str.remove(i - 5);
                str.remove(i - 5);
                JOptionPane.showMessageDialog(null, "The account has been removed safely");
                break;
            }
        }
        
        try{
        BufferedWriter wr = new BufferedWriter(new FileWriter(file));
        for(String k :str){
            wr.write(k + "\r\n");
        }
        wr.write("\r\n");
        wr.close();    
        }
        catch(IOException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_DeleteMouseClicked

