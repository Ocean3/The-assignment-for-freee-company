    private void AuthentificationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AuthentificationMouseClicked
        String option = JOptionPane.showInputDialog(null, "Which accout you want up to Admin?\nPlease type user name.");
        File file = new File("Textfiles/Customer.txt");
        ArrayList<String> str = take_out(file);
        for(int i = 0; i < str.size(); i++){
            if(str.get(i).equals(option)){
                str.set(i + 1, "Admin");
                JOptionPane.showMessageDialog(null, "The account has been changed safely");
                break;
            }
        }
        try{
        BufferedWriter wr = new BufferedWriter(new FileWriter(file));
        for(String k :str){
            wr.write(k + "\r\n");
        }
        wr.write("\r\n");
        wr.close();    
        }
        catch(IOException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_AuthentificationMouseClicked

